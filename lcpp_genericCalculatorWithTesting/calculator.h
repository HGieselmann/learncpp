#ifndef CALCULATOR
#define CALCULATOR

template <typename T, typename U>
auto add(T x, U y)
{
    return x + y;
}

template <typename T, typename U>
auto subtract(T x, U y)
{
    return x - y;
}

template <typename T, typename U>
auto multiply(T x, U y)
{
    return x * y;
}

template <typename T, typename U>
auto divide(T x, U y)
{
    return x / y;
}



#endif
